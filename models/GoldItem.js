class GoldItem {
  constructor(id, name, amountOfGold, price, category, thumbNail, images) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.category = category;
    this.thumbNail = thumbNail;
    this.images = images;
    this.amountOfGold = amountOfGold;
  }
}

export default GoldItem;
